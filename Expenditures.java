    private void btnnCreateExpenditureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnCreateExpenditureActionPerformed
         
        ExpenditureDB exDB = new ExpenditureDB();
        try {
             if (txtFldDescription.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, " Description should not be blank!", "Error!", JOptionPane.OK_OPTION);
                txtFldDescription.requestFocus();
            }  else if (txtFldAmountExpenses.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, " Total Expenses should not be blank!", "Error!", JOptionPane.OK_OPTION);
                txtFldAmountExpenses.requestFocus();
            } else if (txtFldGrandTotalExpenditure.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, " Please compute the GrandTotal Expenditure!", "Error!", JOptionPane.OK_OPTION);
                txtFldGrandTotalExpenditure.requestFocus();
            }   else if (date.getDateFormatString().length() == 0) {
                JOptionPane.showMessageDialog(null, " Date should not be blank!", "Error!", JOptionPane.OK_OPTION);
                date.requestFocus();
            } else if (exDB.addExpenditurePay( txtFldDescription.getText(), txtFldAmountExpenses.getText(),  year.getYear(), comboBoxMonth.getSelectedItem().toString(), date.getDate().toString(), txtFldGrandTotalExpenditure.getText())) {
                JOptionPane.showMessageDialog(null, "Expenditure Record Registered Successfully!");
                resetDetails();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error!", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnnCreateExpenditureActionPerformed

