    Object[] columns = {"Description", "Expenses Amount",};
    Object[] rows = new Object[1000000];

    DefaultTableModel model = new DefaultTableModel();

    private void btnAddNewExpenditureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewExpenditureActionPerformed

        model.setColumnIdentifiers(columns);
        jTable1.setModel(model);

        rows[0] = txtFldDescription.getText();
        rows[1] = txtFldAmountExpenses.getText();
      

        try
        {
           if (txtFldDescription.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Payment Description should not be blank!", "Error!", JOptionPane.OK_OPTION);
            txtFldDescription.requestFocus();
        }else if (txtFldAmountExpenses.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Total Amount used in Expenses should not be blank!", "Error!", JOptionPane.OK_OPTION);
            txtFldAmountExpenses.requestFocus();
        }
        else 
        {
            model.addRow(rows);
        }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Incorrect values!", "Error!", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnAddNewExpenditureActionPerformed

    private void btnDeleteExpenditureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteExpenditureActionPerformed
        
        
        int i = jTable1.getSelectedRow();

        if (i >= 0) {
            model.removeRow(i);
        } else {
            JOptionPane.showMessageDialog(null, "Empty Values!", "Error!", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnDeleteExpenditureActionPerformed

